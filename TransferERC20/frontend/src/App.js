function App() {
  const [account, setAccount] = useState("");
  const [receiver, setReceiver] = useState("");
  const [amount, setAmount] = useState("");
  const [status, setStatus] = useState("");

  const connectWallet = async() => {
    const [acc] = await window.ethereum.request({ method: "eth_requestAccounts"});
    setAccount(acc);
  }

  // ye function token ko transfer krega connected account se receiver account ko.
  const sendToken = async () => {
    if(!receiver || !amount){
      return alert("Enter receiver and amount!");
    }
    
    // in 3 line ki help se hum smart contract se connect hoge.
    // metamask browser me ethereum object inject kr deta h.
    const provider = new ethers.BrowserProvider(window.ethereum);
    // hum signer lenge provider se jo transaction ko sign krega.
    const signer = await provider.getSigner();
    // ye line ehters library ko contract se connect kr rhi h. Ab hum token ki help se smart contract k function call kr skte h.
    const token = new ethers.Contract(contractAddress, contractABI, signer);

    try {
      // hamara likha gya smart contract ERC20 se inherit ho rha h aur us ERC20 smart contract k andar transfer function h.
      const tx = await token.transfer(receiver, ethers.parseUnits(amount, 18));
      await tx.wait();
      setStatus("Token transferred!");
    } catch (error) {
      console.log(error);
      setStatus(" Transfer Failed!");
    }
  }
  return (
    <div className="App">
      <h2>UV Token Transfer</h2>
      {
        !account ? (
          <button onClick={connectWallet}>
            Connect Wallet
          </button>
        ) : (
          <p>Connected: {account}</p>
        )
      }
      <input type="text" placeholder="Receiver Address" onChange={(e) => setReceiver(e.target.value)} />
      <input type="text" placeholder="Amount" onChange={(e) => setAmount(e.target.value)} />
      <button onClick={sendToken}>Send Token</button>
      <p>{status}</p>
    </div>
  );
}

export default App;
